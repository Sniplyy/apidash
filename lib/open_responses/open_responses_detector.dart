/// to render the same response.
String generateFlutterSnippet(String jsonBody) {
  const tq = "'''"; // Triple-quote delimiter — avoids escape lint warnings.
  return """
// ─────────────────────────────────────────────────────────────────────────────
// Generated by APIDash — Open Responses Visualizer
// Paste this into your Flutter project to render this LLM response.
//
// 1. Add the dependency to pubspec.yaml (when published):
//    open_responses_ui: ^0.1.0
//
// 2. Use the widget:
// ─────────────────────────────────────────────────────────────────────────────

import 'package:flutter/material.dart';

const String kResponseJson = r$tq
$jsonBody
$tq;

class MyResponseView extends StatelessWidget {
  const MyResponseView({super.key});

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text('AI Response')),
      body: OpenResponsesViewer(jsonBody: kResponseJson),
    );
  }
}
""";
}
